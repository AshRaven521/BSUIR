package Models;

public class RepairOrder
{
    private int repairOrderId;
    private String name;
    private int duration;
    private int cost;

    public int getRepairOrderId()
    {
        return repairOrderId;
    }

    public String getName()
    {
        return name;
    }

    public int getDuration()
    {
        return duration;
    }

    public int getCost()
    {
        return cost;
    }
}
